# ── Colour palette mirrors frontend index.css tokens ──
ACCENT = "FF467273"
ACCENT_SOFT = "FFE3EBEB"
TEXT_MUTED = "FF5E6C84"
BORDER = "FFDFE1E6"
BG_ROW_ALT = "FFF7F8FA"
ORANGE = "FFEA580C"
GREEN = "FF16A34A"

# ── Fonts ──
FONT_BRAND = Font(name="Inter", size=14, bold=True, color="FFFFFFFF")
FONT_SUB = Font(name="Inter", size=10, color="FFFFFFFF")
FONT_H2 = Font(name="Inter", size=12, bold=True, color="FF1A1A2E")
FONT_TH = Font(name="Inter", size=10, bold=True, color=TEXT_MUTED)
FONT_TD = Font(name="Inter", size=10, color="FF1A1A2E")
FONT_TD_ACCENT = Font(name="Inter", size=10, bold=True, color=ACCENT)
FONT_MONO = Font(name="Consolas", size=10, color="FF1A1A2E")
FONT_FOOT = Font(name="Inter", size=9, italic=True, color=TEXT_MUTED)

# ── Fills / borders ──
FILL_ACCENT = PatternFill("solid", fgColor=ACCENT)
FILL_SUB = PatternFill("solid", fgColor=ACCENT_SOFT)
FILL_ALT = PatternFill("solid", fgColor=BG_ROW_ALT)
THIN = Side(style="thin", color=BORDER)
BOX = Border(top=THIN, bottom=THIN, left=THIN, right=THIN)

INR_FMT = '_-"₹"* #,##,##0.00_-;-"₹"* #,##,##0.00_-;_-"₹"* "-"??_-;_-@_-'
INT_FMT = "#,##,##0"


def _safe_date(raw: str | None) -> date | None:
    if not raw:
        return None
    try:
        return datetime.fromisoformat(str(raw)).date()
    except ValueError:
        return None


def _brand_header(ws: Worksheet, title: str, subtitle: str = "", cols: int = 6) -> int:
    """Write the 2-row Lekha-branded header. Returns the next free row index."""
    ws.merge_cells(start_row=1, start_column=1, end_row=1, end_column=cols)
    cell = ws.cell(row=1, column=1, value=f"Lekha AI  ·  {title}")
    cell.font = FONT_BRAND
    cell.fill = FILL_ACCENT
    cell.alignment = Alignment(horizontal="left", vertical="center", indent=1)
    ws.row_dimensions[1].height = 28

    if subtitle:
        ws.merge_cells(start_row=2, start_column=1, end_row=2, end_column=cols)
        cell = ws.cell(row=2, column=1, value=subtitle)
        cell.font = FONT_SUB
        cell.fill = FILL_ACCENT
        cell.alignment = Alignment(horizontal="left", vertical="center", indent=1)
        ws.row_dimensions[2].height = 18
        return 4  # skip a blank row

    return 3


def _footer(ws: Worksheet, row: int, cols: int = 6) -> None:
    ws.merge_cells(start_row=row, start_column=1, end_row=row, end_column=cols)
    cell = ws.cell(
        row=row,
        column=1,
        value=(
            "Generated by Lekha AI · TDS Calculator. Computational assistance only — "
            "final TDS responsibility rests with the deductor. scaleupcfo.in"
        ),
    )
    cell.font = FONT_FOOT
    cell.alignment = Alignment(horizontal="left", vertical="center", indent=1)


def _write_header_row(ws: Worksheet, row: int, labels: list[str]) -> None:
    for col_idx, label in enumerate(labels, start=1):
        c = ws.cell(row=row, column=col_idx, value=label)
        c.font = FONT_TH
        c.fill = FILL_SUB
        c.alignment = Alignment(horizontal="left" if col_idx <= 2 else "right", vertical="center")
        c.border = BOX


def _write_data_row(
    ws: Worksheet,
    row: int,
    values: list[Any],
    numeric_cols: set[int] = frozenset(),
    accent_cols: set[int] = frozenset(),
    mono_cols: set[int] = frozenset(),
    alt: bool = False,
) -> None:
    for col_idx, value in enumerate(values, start=1):
        c = ws.cell(row=row, column=col_idx, value=value)
        if col_idx in mono_cols:
            c.font = FONT_MONO
        elif col_idx in accent_cols:
            c.font = FONT_TD_ACCENT
        else:
            c.font = FONT_TD
        c.alignment = Alignment(
            horizontal="right" if col_idx in numeric_cols else "left",
            vertical="center",
        )
        c.border = BOX
        if alt:
            c.fill = FILL_ALT
        if col_idx in numeric_cols:
            c.number_format = INR_FMT


def _autosize(ws: Worksheet, widths: list[int]) -> None:
    for i, w in enumerate(widths, start=1):
        ws.column_dimensions[get_column_letter(i)].width = w


# Totals row — visually distinct strip at the bottom of each table.
# `values` is the same shape as `_write_data_row`; pass "" / 0 in cells you don't sum.
FILL_TOTAL = PatternFill("solid", fgColor=ACCENT_SOFT)
FONT_TOTAL_LABEL = Font(name="Inter", size=10, bold=True, color="FF1A1A2E")
FONT_TOTAL_NUM = Font(name="Inter", size=10, bold=True, color=ACCENT)
TOTAL_TOP = Border(top=Side(style="medium", color=ACCENT), bottom=THIN, left=THIN, right=THIN)


def _write_totals_row(
    ws: Worksheet,
    row: int,
    values: list[Any],
    numeric_cols: set[int] = frozenset(),
    accent_cols: set[int] = frozenset(),
) -> None:
    """Render a bold totals row with an accent top border."""
    for col_idx, value in enumerate(values, start=1):
        c = ws.cell(row=row, column=col_idx, value=value)
        if col_idx in accent_cols:
            c.font = FONT_TOTAL_NUM
        else:
            c.font = FONT_TOTAL_LABEL
        c.alignment = Alignment(
            horizontal="right" if col_idx in numeric_cols else "left",
            vertical="center",
        )
        c.fill = FILL_TOTAL
        c.border = TOTAL_TOP
        if col_idx in numeric_cols:
            c.number_format = INR_FMT


