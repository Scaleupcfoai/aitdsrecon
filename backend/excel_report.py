def generate_report(tds_results: dict[str, Any]) -> bytes:
    """Build the 5-sheet workbook and return its bytes."""
    results = list(tds_results.get("results") or [])
    flags = list(tds_results.get("flags") or [])
    views = build_views(tds_results)
    payload = {"results": results, "flags": flags, "views": views}

    wb = Workbook()
    # Default sheet becomes Summary
    ws = wb.active
    ws.title = "Summary"
    _sheet_summary(ws, payload)

    _sheet_entries(wb.create_sheet("Entries"), payload)
    _sheet_flagged(wb.create_sheet("Flagged"), payload)
    _sheet_deposit_schedule(wb.create_sheet("Deposit Schedule"), payload)
    _sheet_lekha_recons(wb.create_sheet("Lekha AI · 15 Recons"))

    buf = BytesIO()
    wb.save(buf)
    return buf.getvalue()


def generate_report_file(tds_results: dict[str, Any], out_path: Path) -> Path:
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_bytes(generate_report(tds_results))
    return out_path
