"""Branded 5-sheet Excel report generator.

Sheets:
  1. Summary          party / section / quarter mini-tables + total TDS
  2. Entry-level      one row per expense with TDS calc + resolved flag note
  3. Flagged          unresolved flags, one row each with reason + context
  4. Deposit Schedule quarter-wise TDS with Rule 30 due dates
  5. Lekha AI Recons  marketing page listing all 15 reconciliation modules

Branding: accent #467273 header bar + Lekha AI footer on every sheet.
Indian number formatting (#,##,##0.00) for all rupee columns.
"""

